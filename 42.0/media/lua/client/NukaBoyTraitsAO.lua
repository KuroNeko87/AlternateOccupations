-- Hermit Trait: Player will get panicked when close to other players in MP

function HermitTrait()
        if not isClient() then return end
        local player = getPlayer()
        if not (player and player:HasTrait("Solitary")) then return end
        local hermitPanicMult = 20 -- SandboxVars.Nuka.hermitPanic or 10
        local socialDistance = 12
        local onlineUsers = getOnlinePlayers()
        for i = 0, onlineUsers:size() - 1 do
                local chr = onlineUsers:get(i)
                if player ~= chr then
                        if player:DistTo(chr) <= socialDistance and
                            not player:getSquare():isBlockedTo(chr:getSquare()) then
                                player:getStats():setPanic(player:getStats():getPanic() + hermitPanicMult)
                        end
                end
        end
end

Events.EveryOneMinute.Add(HermitTrait)